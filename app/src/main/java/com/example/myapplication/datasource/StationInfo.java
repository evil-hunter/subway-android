package com.example.myapplication.datasource;

public class StationInfo {
    private int code;
    private String name;
    private int line;
    private String pinyin;
    private String stationLogo;

    public int getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public int getLine() {
        return line;
    }

    public String getPinyin() {
        return pinyin;
    }

    public String getStationLogo() {
        return stationLogo;
    }
}
